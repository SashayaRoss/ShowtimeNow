import UIKit

extension UIColor {
    static func darkBlue() -> UIColor {
        UIColor(red: 0.05, green: 0.14, blue: 0.25, alpha: 1.00)
    }
    
    static func turquoise() -> UIColor {
        UIColor(red: 0.35, green: 0.76, blue: 0.68, alpha: 1.00)
    }

    static func customtGray() -> UIColor {
        UIColor(red: 0.96, green: 0.96, blue: 0.96, alpha: 1.00)
    }
}

